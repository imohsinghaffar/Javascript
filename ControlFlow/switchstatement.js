let month = "August";
switch (month) {
    case "Jan":
        console.log("Your month of birth is January");
    case "Feb":
        console.log("Your month of birth is Feb");
    case "March":
        console.log("Your month of birth is March");
    case "April":
        console.log("Your month of birth is April");
    case "August":
        console.log("Your month of birth is August");
        break;
    case "Sep":
        console.log("Your month of birth is September");
        
    default:
        console.log("Don't find your month of birth!");
        break;
}


